import streamlit as st
import pandas as pd

st.title("VR-Парк: Генератор сценариев")

# 1. Выбор режима
mode = st.radio("Режим планирования:", ["Готовый формат", "Индивидуальный"])

if mode == "Готовый формат":
    # Выпадающий список готовых форматов
    format_type = st.selectbox("Выберите формат:", ["Частый", "Активный", "Сбалансированный"])
    st.write(f"Выбран: {format_type}")
    
elif mode == "Индивидуальный":
    # Поля для ручного ввода
    st.write("Добавьте зоны для сценария:")
    zones = st.multiselect("Выберите зоны:", ["VR-Арена", "Симулятор гонок", "Поздравление", "Комната отдыха"])
    st.write(f"Выбрано зон: {len(zones)}")

# Время начала и кнопка
start_time = st.time_input("Время начала:", value=pd.to_datetime("12:00").time())

if st.button("Сгенерировать 3 варианта"):
    st.write("Генерация запущена для:", mode)
